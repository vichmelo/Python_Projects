# Types int and float 
# int -> integer number
# The type int represents any number
# Positive or negative. int without signal is considered positive
print(11) #int
print(-11) # int
print(0) #int

# float -> number with float dot
# The type float represents any number 
# Positive or ngeative with float dot 
# float without signal is considered positive
print(1.1, 10.11) #float
print(0.0, -1.5) # float

