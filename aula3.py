# Single quotation mark
print('Victor Hugo')
print(1, 'Victor "Hugo"')

# Double quotation mark
print("Victor Hugo")
print(2, "Victor 'Hugo'")

# Escape char, when you need the interpretator does not read some specific char you can use a escape char, in this case '\'
print("Victor \"Hugo\"")

# r, when you need to show the escape char, you can use 'r'
print(r"Victor \"Hugo\"")


